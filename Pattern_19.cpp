#include<bits/stdc++.h>
using namespace std;

int main(){
    int i=1,j,n,spaces;
    cout<<"Enter Number: ";
    cin>>n;
    for(i=1;i<=n;i++)
        {
            spaces=n;
        for(j=1;j<=spaces;j++)
        {    
            cout<<'*';
            spaces=-1;


        }
        cout<<endl;
    }
}